    void centrarventana(JInternalFrame frame){/**los psrsmetros son el internal frame y se le da un nombre**/
      ventanaprincipal.add(frame);
        Dimension dimencion= ventanaprincipal.getSize();//dimencion del la entana principal
        Dimension fventas=  frame.getSize(); // dimencion ventana intena de venta
        frame.setLocation((dimencion.width- fventas.height )/2, (dimencion.height-fventas.width )/2);
   
      frame.show();
    }//GEN-LAST:event_fventaActionPerformed

